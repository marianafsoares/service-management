
package utils.pyAfip;


public class Tributo {

    private int tributo_id;
    private int alic;
    private int base_imp;
    private int importe;
    private String desc;

    public int getTributo_id() {
        return tributo_id;
    }

    public void setTributo_id(int tributo_id) {
        this.tributo_id = tributo_id;
    }

    public int getAlic() {
        return alic;
    }

    public void setAlic(int alic) {
        this.alic = alic;
    }

    public int getBase_imp() {
        return base_imp;
    }

    public void setBase_imp(int base_imp) {
        this.base_imp = base_imp;
    }

    public int getImporte() {
        return importe;
    }

    public void setImporte(int importe) {
        this.importe = importe;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
    
}

